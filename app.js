// Функція для чистих імен у посиланнях
function cleanName(name) {
    return name.toLowerCase().replace(/[^a-zа-я0-9]/g, '');
}

// Головна сторінка
app.get('/', (req, res) => {
    res.sendFile(path.join(__dirname, 'index.html'));
});

// Список студентів
app.get('/students', (req, res) => {
    res.render('students', { students });
});

// Сторінка конкретного студента
app.get('/student/:name', (req, res) => {
    const studentName = req.params.name;
    const student = students.find(s => cleanName(s.name) === studentName);
    if (student) {
        res.render('student', { student, students });
    } else {
        res.status(404).send('Студента не знайдено');
    }
});

app.listen(PORT, () => {
    console.log(`Сервер працює на http://localhost:${PORT}`);
});